package java.OracleOCP.Questions;

public class Platypus {
    String name;
    int sayi;

    public Platypus(String name, int sayi) {
        this.name = name;
        this.sayi = sayi;
    }

    public String getName() {
        return name;
    }

    public int getSayi() {
        return sayi;
    }
}
